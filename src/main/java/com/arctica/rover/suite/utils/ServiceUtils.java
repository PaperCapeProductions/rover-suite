package com.arctica.rover.suite.utils;

import java.util.concurrent.ExecutorService;
import java.util.concurrent.Executors;

public class ServiceUtils {
	// =============================================================================================
	// CONSTANTS
	// =============================================================================================

	// =============================================================================================
	// VARIABLES
	// =============================================================================================
	private static ExecutorService _service = null;

	// =============================================================================================
	// CONSTRUCTORS
	// =============================================================================================

	// =============================================================================================
	// PUBLIC METHODS
	// =============================================================================================
	public static ExecutorService getExecutorService() {
		if(_service == null) {
			_service = Executors.newCachedThreadPool();
		}
		return _service;
	}

	// =============================================================================================
	// DEFAULT METHODS
	// =============================================================================================

	// =============================================================================================
	// PROTECTED METHODS
	// =============================================================================================

	// =============================================================================================
	// PRIVATE METHODS
	// =============================================================================================

	// =============================================================================================
	// INNER CLASSES
	// =============================================================================================


}
